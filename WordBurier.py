    @loader.command()
    async def bury(self, message: Message):
        """–£–Ω–∏—á—Ç–æ–∂–∏—Ç—å –≤—Ä–∞–≥–∞ –≤ –ø—É—Ö –∏ –ø—Ä–∞—Ö - –æ—Ç–≤–µ—Ç—å –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ"""
        reply = await message.get_reply_message()
        if not reply or not reply.sender:
            await utils.answer(message, self.strings["no_reply"])
            return

        enemy = reply.sender
        enemy_name = getattr(enemy, "first_name", "–•–µ–π—Ç–µ—Ä") or "–ê–Ω–æ–Ω–∏–º"
        
        await utils.answer(message, self.strings["processing"])
        
        # –í—ã–±–∏—Ä–∞–µ–º 3 —Å–ª—É—á–∞–π–Ω—ã—Ö –º–µ—Ç–æ–¥–∞ —É–Ω–∏—á—Ç–æ–∂–µ–Ω–∏—è
        used_methods = random.sample(self.phrases, 3)
        methods_text = "\n".join(f"‚Ä¢ {method}" for method in used_methods)
        
        # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é —ç–ø–∏—Ç–∞—Ñ–∏—é
        epitaph = random.choice(self.epitaphs).format(name=enemy_name)
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        await utils.answer(
            message,
            self.strings["result"].format(
                enemy_name=enemy_name,
                methods=methods_text,
                epitaph=epitaph
            )
        )

